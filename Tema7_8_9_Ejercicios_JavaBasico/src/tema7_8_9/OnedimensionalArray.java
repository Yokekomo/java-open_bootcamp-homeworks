package Tema7_8_9_Ejercicios_JavaBasico;

public class OnedimensionalArray {
    public static void main(String[] args) {
        String array[] = {
                "Juan",
                "Pedro",
                "Maria"
        };

        for(int i=0; i<array.length; i++) {
            System.out.println(i);
        }
    }
}
